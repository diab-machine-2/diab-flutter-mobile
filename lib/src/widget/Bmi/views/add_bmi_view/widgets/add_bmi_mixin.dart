  int findIndexInRanges({
    required AddBmiCubit cubit,
  }) {
    List<double> ranges = cubit.rangeValue;
    if (cubit.selectedWeight == null) return -1;

    for (int i = 0; i < ranges.length - 1; i++) {
      if (cubit.selectedWeight >= ranges[i] &&
          cubit.selectedWeight <= ranges[i + 1]) {
        return i;
      }
    }
    return ranges.length - 1;
  }

